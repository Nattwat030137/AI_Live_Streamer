"""ทดสอบเสียงภาษาไทยจาก OpenAI."""

from app.voice_engine import speak


def main() -> None:
    """สร้างและเล่นเสียงทดสอบ."""

    test_text = (
        "สวัสดีค่ะ ยินดีต้อนรับเข้าสู่ไลฟ์ของร้านเบเกอรี่ ดีเวอร์ "
        "ทางร้านจัดส่งสินค้าทั่วประเทศไทยค่ะ "
        "สนใจเมนูไหน สอบถามได้เลยนะคะ"
    )

    print("กำลังสร้างเสียง...")
    speak(test_text)
    print("ทดสอบเสียงเสร็จแล้ว")


if __name__ == "__main__":
    main()